    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        Pelicula pelicula;
        String nombrePeli = txtNombre.getText();
        String nacionalidad;
        String duracion;
        String director;
        String interpretes;
        String argumento;
        String genero = cBoxGenero.getSelectedItem().toString();
        String clasificacion = cBClasificacion.getSelectedItem().toString();

        if ("".equals(nombrePeli)) {
            JOptionPane.showMessageDialog(this, "El nombre no puede estar vacio");
        } else {
            nacionalidad = txtNacionalidad.getText();
            if ("".equals(nacionalidad)) {
                JOptionPane.showMessageDialog(this, "La nacionalidad no puede estar vacia");
            } else {
                duracion = txtDuracion.getText();
                if ("".equals(duracion)) {
                    JOptionPane.showMessageDialog(this, "La duracion no puede estar vacia");
                } else {
                    director = txtDirector.getText();
                    if ("".equals(director)) {
                        JOptionPane.showMessageDialog(this, "El director no puede estar vacio");
                    } else {
                        interpretes = txtInterpretes.getText();
                        if ("".equals(interpretes)) {
                            JOptionPane.showMessageDialog(this, "El interprete no puede estar vacio");
                        } else {
                            argumento = txtArgumento.getText();
                            if ("".equals(argumento)) {
                                JOptionPane.showMessageDialog(this, "El argumento no puede estar vacio");
                            } else {
                                pelicula = controlador.Cine.buscarPelicula(comboPelicula.getSelectedItem().toString());
                                pelicula.modificarPelicula(nombrePeli, nacionalidad, duracion, director, interpretes, argumento, genero, clasificacion);
                                this.dispose();
                            }
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void cargarComboGenero() {
        Genero[] genero;
        genero = Genero.values();

        for (Genero generoAux : genero) {
            cBoxGenero.addItem(generoAux.name());
        }
    }

    private void cargarComboClasificacion() {
        Clasificacion[] clasificacion;
        clasificacion = Clasificacion.values();

        for (Clasificacion clasificacionAux : clasificacion) {
            cBClasificacion.addItem(clasificacionAux.name());
        }
    }

    public void cargarComboPeliculas() {
        ArrayList<Pelicula> listaPeliculas = controlador.Cine.obtenerPeliculas();

        if (!listaPeliculas.isEmpty()) {
            for (Pelicula pelicula : listaPeliculas) {
                comboPelicula.addItem(pelicula.getNombrePeli());
            }
        }
    }

    public void cargarDatos() {
        ArrayList<Pelicula> listaPelicula = controlador.Cine.obtenerPeliculas();

        if (!listaPelicula.isEmpty()) {
            String nombrePelicula = comboPelicula.getSelectedItem().toString();
            Pelicula pelicula = controlador.Cine.buscarPelicula(nombrePelicula);
            txtNombre.setText(pelicula.getNombrePeli());
            txtNacionalidad.setText(pelicula.getNacionalidad());
            txtDuracion.setText(pelicula.getDuracion());
            txtDirector.setText(pelicula.getDirector());
            txtInterpretes.setText(pelicula.getInterpretes());
            txtArgumento.setText(pelicula.getArgumento());
            cBoxGenero.setSelectedItem(pelicula.getGenero());
            cBClasificacion.setSelectedItem(pelicula.getClasificacion());
        }
    }

